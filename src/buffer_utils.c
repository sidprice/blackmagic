#include "general.h"

bool check_command_args(const char *const input, uint32_t *result)
{
	char *end_pointer = NULL;

	*result = strtoul(input, &end_pointer, 0);
	return end_pointer != NULL && strcmp(end_pointer, input) != 0;
}
